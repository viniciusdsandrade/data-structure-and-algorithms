    public static void rotate(int[][] matriz) {

        if (matriz.length < 1 || matriz.length > 20)
            throw new IllegalArgumentException("A matriz deve ter tamanho entre 1 e 20");

        // Se a matriz tiver tamanho 1, não é necessário rotacionar
        if (matriz.length == 1) return;


        for (int i = 0; i < matriz.length; i++) {
            for (int j = i; j < matriz.length; j++) {
                swap(matriz, i, j); // Transpõe a matriz
            }
        }

        // Inverte cada linha para obter a matriz final rotacionada
        for (int i = 0; i < matriz.length; i++) {
            for (int j = 0; j < matriz.length / 2; j++) {
                swapVertically(matriz, i, j);// Troca os elementos ao longo do eixo vertical
            }
        }
    }

    private static void swapVertically(int[][] matriz, int i, int j) {
        int temp = matriz[i][j];
        matriz[i][j] = matriz[i][matriz.length - 1 - j];
        matriz[i][matriz.length - 1 - j] = temp;
    }

    private static void swap(int[][] matriz, int i, int j) {
        int temp = matriz[i][j];
        matriz[i][j] = matriz[j][i];
        matriz[j][i] = temp;
    }

    public static void testRotate(int[][] matrix) {

        System.out.println("\nInput: ");
        printMatrix(matrix);

        long start = System.nanoTime();
        rotate(matrix);
        long end = System.nanoTime();

        System.out.println("Output: ");
        printMatrix(matrix);
        System.out.println("Runtime: " + (end - start) + " ns");
        System.out.println("Runtime: " + (end - start) / 1e6 + " ms\n");
    }

